// Reduce x modulo 360 degrees
function swe_degnorm(x) {
	var y;
	y = x % 360.0;
	if (Math.abs(y) < 1e-13) {
	    y = 0;    // Alois fix 11-dec-1999
	}
	if( y < 0.0 ) {
	    y += 360.0;
	}
	return y;
};

// Ported from swe_radnorm() from swephlib.c
// Reduce x modulo TWOPI degrees
function swe_radnorm(x) {
	var y;
	y = x % (2*Math.PI);
	if (Math.abs(y) < 1e-13) {
	    y = 0;    // Alois fix 11-dec-1999
	}
	if( y < 0.0 ) {
	    y += 2*Math.PI;
	}
	return y;
}

